      subroutine wbb_setreal(p,rflav,amp2)
      implicit none
      include 'src/Lib/wbb/nlegborn.h'
      include 'src/Lib/wbb/pwhg_flst.h'
      include 'src/Lib/wbb/pwhg_math.h'
      include 'src/Lib/wbb/pwhg_st.h'
      include 'src/Lib/wbb/pwhg_kn.h'
      include 'src/Lib/wbb/PhysPars.h'
      include 'src/Lib/wbb/coupl.inc'       ! Madgraph common block 
      real * 8 p(0:3,nlegreal)
      integer rflav(nlegreal)
      real * 8 amp2
      logical ini
      data ini/.true./
      save ini
      real * 8 gs2
      real * 8 ans,amp2_n
      real * 8 wbb_powheginput
      integer idvecbos
      save idvecbos
      if (ini) then
c     wbb_initialize MadGraph couplings
         call wbb_initialize
         idvecbos=wbb_powheginput('idvecbos')
         ini=.false.
      endif
      gs2 = 4*pi*st_alpha
c      gw = ph_unit_e/ph_sthw
c     Set MADGRAPH Wmass to the W virtuality
      WMASS=kn_masses(3)
      
      if (rflav(1).ne.0.and.rflav(2).ne.0) then
         if (idvecbos.eq.-24) then
c     dub,sub,dcb,scb > W^-bbbg
            if (rflav(1).eq.1.or.rflav(1).eq.3) then
               call wbb_SDUB_WMBBBG(p,amp2)
            else
c     ubd,ubs,cbd,cbs > W^-bbbg
               call wbb_SUBD_WMBBBG(p,amp2)
            endif
         elseif (idvecbos.eq.24) then
c     udb,cdb,usb,csb > W^+bbbg 
            if (rflav(1).eq.2.or.rflav(1).eq.4) then
               call wbb_SUDB_WPBBBG(p,amp2)
            else
c     dbu,dbc,sbu,sbc > W+bbbg
               call wbb_SDBU_WPBBBG(p,amp2)
            endif
         endif
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(2)))**2
      elseif(
c     gd > W-bbb u,c
     #        (rflav(1).eq.0.and.rflav(2).eq.1).or.
c     gs > W-bbb u,c
     #        (rflav(1).eq.0.and.rflav(2).eq.3)) then
         call wbb_SGD_WMBBBU(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(6)))**2
      elseif (
c     dg > W-bbb u,c
     #        (rflav(1).eq.1.and.rflav(2).eq.0).or.
c     sg > W-bbb u,c
     #        (rflav(1).eq.3.and.rflav(2).eq.0)) then
         call wbb_SDG_WMBBBU(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(6)))**2
      elseif (
c     gub > W-bbb db,sb
     #        (rflav(1).eq.0.and.rflav(2).eq.-2).or.
c     gcb > W-bbb db,sb
     #        (rflav(1).eq.0.and.rflav(2).eq.-4)) then
         call wbb_SGUB_WMBBBDB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(6)))**2
      elseif (
c     ubg > W-bbb db,sb
     #        (rflav(1).eq.-2.and.rflav(2).eq.0).or.
c     cbg > W-bbb db,sb
     #        (rflav(1).eq.-4.and.rflav(2).eq.0)) then
         call wbb_SUBG_WMBBBDB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(6)))**2
      elseif(
c     gu > W+bbb d,s
     #        (rflav(1).eq.0.and.rflav(2).eq.2).or.
c     gc > W+bbb d,s
     #        (rflav(1).eq.0.and.rflav(2).eq.4)) then
         call wbb_SGU_WPBBBD(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(6)))**2
      elseif (
c     ug > W+bbb d,s
     #        (rflav(1).eq.2.and.rflav(2).eq.0).or.
c     cg > W+bbb d,s
     #        (rflav(1).eq.4.and.rflav(2).eq.0)) then
         call wbb_SUG_WPBBBD(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(6)))**2
      elseif (
c     gdb > W+bbb ub,cb
     #        (rflav(1).eq.0.and.rflav(2).eq.-1).or.
c     gsb > W+bbb ub,cb
     #        (rflav(1).eq.0.and.rflav(2).eq.-3)) then
         call wbb_SGDB_WPBBBUB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(2)),abs(rflav(6)))**2
      elseif (
c     dbg > W+bbb ub,cb
     #        (rflav(1).eq.-1.and.rflav(2).eq.0).or.
c     sbg > W+bbb ub,cb
     #        (rflav(1).eq.-3.and.rflav(2).eq.0)) then
         call wbb_SDBG_WPBBBUB(p,amp2)
         amp2=amp2*ph_CKM_matrix(abs(rflav(1)),abs(rflav(6)))**2
      else
         write(*,*) 'wrong calling sequence in wbb_setreal'
         write(*,*) 'rflav =',rflav
         call exit(1)
      endif

c STRIP OFF a factor of as/(2pi) !!!!!!
      amp2=amp2*gs2**2*(gw/(gwf(1)*dsqrt(2d0)))**2*8*pi**2
      end

