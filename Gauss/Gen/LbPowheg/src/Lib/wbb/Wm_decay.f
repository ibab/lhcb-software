      subroutine wbb_setborn_Wmdecay(p,bflav,born)
      implicit none
      include 'src/Lib/wbb/nlegborn.h'
      include 'src/Lib/wbb/pwhg_flst.h'
      include 'src/Lib/wbb/pwhg_math.h'
      include 'src/Lib/wbb/pwhg_st.h'
      include 'src/Lib/wbb/PhysPars.h'
      include "coupl.inc"       ! Madgraph common block
      real * 8 p(0:3,nlegborn+1)
      integer bflav(nlegborn+1),mu,i
      real * 8 born
      real * 8 gs2,gweak      
      logical ini
      data ini/.true./
      save ini
c      
      if (ini) then
c     wbb_initialize MadGraph couplings
         call wbb_initialize
         ini=.false.
      endif      
      gs2 = 4*pi*st_alpha
      gweak = ph_unit_e/ph_sthw
c     Set MADGRAPH Wmass to the pole W mass
      WMASS=ph_Wmass
      if (bflav(1).eq.1.or.bflav(1).eq.3) then
         call wbb_SDUB_EMVEBBB(p,born)
      elseif (bflav(2).eq.1.or.bflav(2).eq.3) then
         call wbb_SUBD_EMVEBBB(p,born)
      else
         write(*,*) 'Error in W- decay. No Born diagram'
         call exit(1)
      endif         
c     add CKW 
      born = born*ph_CKM_matrix(abs(bflav(1)),abs(bflav(2)))**2
      born = born* (gweak/(gwf(1)*dsqrt(2d0)))**4 * gs2**2
      end

      subroutine wbb_setreal_Wmdecay(p,rflav,amp2)
      implicit none
      include 'src/Lib/wbb/nlegborn.h'
