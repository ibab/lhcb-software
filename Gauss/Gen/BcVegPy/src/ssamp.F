          C=2.0D0*Y(I+1)-1.0D0
          S=DSQRT(1.0D0-C*C)
          F=2.0D0*PI*Y(I+2)
          FAC=Y(I+3)
        END IF
        Q(4,I)=-DLOG(FAC)
        EXTRA_WEIGHT=EXTRA_WEIGHT*Q(4,I)
        Q(3,I)=Q(4,I)*C
        Q(2,I)=Q(4,I)*S*COS(F)
        Q(1,I)=Q(4,I)*S*SIN(F)
      END DO

C CALCULATE THE PARAMETERS OF THE SCALE TRANSFORMATION
      DO I=1,4
        R(I)=0.0D0
      END DO
      DO I=1,N-1
        DO K=1,4
          R(K)=R(K)+Q(K,I)
        END DO
      END DO

C CALCULATE THE N_TH VECTOR
      DO K=1,3
        Q(K,N)=-R(K)
      END DO
      Q(4,N)=DSQRT(Q(1,N)**2+Q(2,N)**2+Q(3,N)**2)
      R(4)=R(4)+Q(4,N)
      RMAS=R(4)
      IF(RMAS .LT. 1.0D-6)THEN
        WT=0.D0
        RETURN
      END IF
      X=ET/RMAS

C scale the q's to the p's
      DO I=1,N
        DO K=1,4
          P(K,I)=X*Q(K,I)
        END DO
      END DO

C RETURN FOR WEIGHTED MASSLESS MOMENTA
      WT=PI2LOG*(N-1)-DLOG(ET-P(4,N))*2.0D0*(1-N)-DLOG(ET)-
     &	DLOG(2.0D0)-DLOG(P(4,N))-Z(2*N-3)
      IF(NM.EQ.0) THEN
        WT=EXTRA_WEIGHT*DEXP(WT)
        RETURN
      END IF

C MASSIVE PARTICLES: RESCALE THE MOMENTA BY A FACTOR X
      XMAX=DSQRT(1.0D0-(XMT/ET)**2)
      DO I=1,N
        XM2(I)=XM(I)**2
        P2(I) =P(4,I)**2
      END DO
      ITER=0
      X=XMAX
      ACCU=ET*ACC
      DO WHILE (ITER.LE.ITMAX)
        F0=-ET
        G0=0.D0
        X2=X*X
        DO I=1,N
          E(I)=DSQRT(XM2(I)+X2*P2(I))
          F0=F0+E(I)
          IF(E(I) .GT. 0.0D0)THEN
            G0=G0+P2(I)/E(I)
          END IF
        END DO
        IF(DBLE(ABS(F0)).GT.ACCU.AND.ITER.LE.ITMAX) THEN
          ITER=ITER+1
          X=X-F0/(X*G0)
        ELSE IF(DBLE(ABS(F0)).LE.ACCU) THEN
          ITER=ITMAX+1
        END IF
      END DO

      DO I=1,N
        V(I)=X*P(4,I)
        DO K=1,3
          P(K,I)=X*P(K,I)
        END DO
        P(4,I)=E(I)
      END DO

C CALCULATE THE MASS-EFFECT WEIGHT FACTOR
      WT2=1.0D0
      WT3=0.0D0
      DO I=1,N
        IF(E(I) .GT. 0.0D0)THEN
          WT2=WT2*V(I)/E(I)
          WT3=WT3+V(I)**2/E(I)
        END IF
      END DO
      WTM=(2.0D0*N-3.0D0)*DLOG(X)+DLOG(WT2/WT3*ET)

C RETURN FOR WEIGHTED MASSIVE MOMENTA
      WT=WT+WTM
      WT=DEXP(WT)*EXTRA_WEIGHT
      IF(XMT .GT. ET)THEN
        WT=0.0D0
        RETURN
      END IF

C...B_C 
      PMOMUP(5,3)=XM(3)
      DO J=1,4
	   PMOMUP(J,3)=P(J,3)
      END DO

C...B QUARK
      PMOMUP(5,4)=XM(2)
      DO J=1,4
	   PMOMUP(J,4)=P(J,2)
      END DO
     
C...\BAR{C} QUARK     
      PMOMUP(5,5)=XM(1)
      DO J=1,4
	   PMOMUP(J,5)=P(J,1)
      END DO

      RETURN
      END

